package session_3_java_operators;

public class LogicalOP {
    public static void main(String[] args) {

        boolean prez=true;
        boolean registred=false;

        System.out.println(!prez);
        System.out.println(!registred);

        int valA = 3;
        int valB = 2;

        System.out.println(valA>valB);
        System.out.println(valA<valB);

    }
}
